//This is the simplest program in c 
//program to find sum in two numbers

#include<stdio.h>
void main () {
  int num1, num2, sum;
  printf("Enter two numbers : ");
  scanf("%d%d", &num1, &num2);
  sum = num1 + num2;
  printf("Result = %d", sum);
}

